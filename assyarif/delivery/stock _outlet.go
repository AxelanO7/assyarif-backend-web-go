package delivery

import (
	"assyarif-backend-web-go/domain"
	"strconv"

	"github.com/asaskevich/govalidator"
	"github.com/gofiber/fiber/v2"
)

type StockOutletHandler struct {
	StockOutletUC domain.StockOutletUseCase
}

func NewStockOutletHandler(c *fiber.App, das domain.StockOutletUseCase) {
	handler := &StockOutletHandler{
		StockOutletUC: das,
	}
	api := c.Group("/stock_outlet")

	_ = api.Group("/public")

	private := api.Group("/private")
	private.Get("/stuff", handler.GetAllStockOutlet)
	private.Get("/stuff/:id", handler.GetStockOutletByID)
	private.Post("/stuff", handler.CreateStockOutlet)
	private.Put("/stuff/:id", handler.UpdateStockOutlet)
	private.Delete("/stuff/:id", handler.DeleteStockOutlet)

	private.Post("/increase-dashboard", handler.IncreaseDashboard)
	private.Post("/decrease-dashboard", handler.DecreaseDashboard)
}

func (t *StockOutletHandler) GetAllStockOutlet(c *fiber.Ctx) error {
	res, err := t.StockOutletUC.FetchStockOutlets(c.Context())
	if err != nil {
		return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": err,
			"error":   err.Error(),
		})
	}
	return c.Status(fiber.StatusOK).JSON(fiber.Map{
		"status":  200,
		"success": true,
		"data":    res,
		"message": "Successfully get all user",
	})
}

func (t *StockOutletHandler) GetStockOutletByID(c *fiber.Ctx) error {
	id := c.Params("id")
	strId, erStr := strconv.Atoi(id)
	if erStr != nil {
		return c.Status(fiber.StatusUnprocessableEntity).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": "Failed to parse id",
			"error":   erStr.Error(),
		})
	}
	res, err := t.StockOutletUC.FetchStockOutletByID(c.Context(), uint(strId))
	if err != nil {
		return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": err,
			"error":   err.Error(),
		})
	}
	return c.Status(fiber.StatusOK).JSON(fiber.Map{
		"status":  200,
		"success": true,
		"data":    res,
		"message": "Successfully get user by id",
	})
}

func (t *StockOutletHandler) CreateStockOutlet(c *fiber.Ctx) error {
	req := new(domain.StockOutlet)
	if err := c.BodyParser(req); err != nil {
		return c.Status(fiber.StatusUnprocessableEntity).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": "Failed to parse body",
			"error":   err,
		})
	}
	valRes, er := govalidator.ValidateStruct(req)
	if !valRes {
		return c.Status(fiber.StatusUnprocessableEntity).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": "Failed to parse body",
			"error":   er.Error(),
		})
	}
	res, err := t.StockOutletUC.CreateStockOutlet(c.Context(), req)
	if err != nil {
		return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": err,
			"error":   err.Error(),
		})
	}
	return c.Status(fiber.StatusCreated).JSON(fiber.Map{
		"status":  201,
		"success": true,
		"data":    res,
		"message": "Successfully create user",
	})
}

